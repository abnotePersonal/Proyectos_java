
public class condicional {

	public static void main(String[] args) {
		int cont2=1;
		for (int cont=0;cont<20;cont++) {
			System.out.println(cont2);
			cont2++;
		}
		int cont=1;
		while (cont<=20) {
			System.out.println(cont);
			cont++;
		}
		cont=0;
		do {
			System.out.println(cont);
			cont++;
		}while(cont<=20);

	}

}
